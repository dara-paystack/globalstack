// ─── Shared payload templates ─────────────────────────────────────────────────
// Each delivery has a payload field — a truncated preview of what was POSTed.
// In a real system this would be the verbatim request body. Here we snapshot
// enough fields to make it legible (~8 lines when pretty-printed).

function conversionPayload(id, corridor, amount, rate, sourceAmount, sourceCurrency, ts) {
  return {
    event: 'conversion.completed',
    id,
    corridor,
    amount,
    currency: 'USDC',
    rate,
    sourceAmount,
    sourceCurrency,
    settledAt: ts,
  }
}

function transferPayload(id, amount, destination, chain, ts) {
  return {
    event: 'transfer.completed',
    id,
    amount,
    currency: 'USDC',
    destination,
    chain,
    txHash: '0xd4f5a2b1...8a21cc90',
    settledAt: ts,
  }
}

function depositPayload(id, amount, method, account, ref, ts) {
  return {
    event: 'deposit.indexed',
    id,
    amount,
    currency: 'USD',
    method,
    account,
    reference: ref,
    indexedAt: ts,
  }
}

